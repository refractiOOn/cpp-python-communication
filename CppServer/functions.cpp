#include "pch.hpp"
#include "functions.hpp"

int Plus(int a, int b)
{
	return a + b;
}

int Minus(int a, int b)
{
	return a - b;
}

int Multiply(int a, int b)
{
	return a * b;
}

int Divide(int a, int b)
{
	return a / b;
}

std::string Concat(const std::string &s1, const std::string &s2)
{
	return s1 + s2;
}